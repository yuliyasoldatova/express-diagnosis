//  ГЛАВНАЯ ЛОГИКА: ЗАПУСК ДИАГНОСТИКИ
// ============================================================
function startDiagnostic() {
  // 1. Скрыть приветствие, показать экран загрузки
  showScreen("screen-loading");

  // 2. Выждать 1.8 секунды (анимация «настройки»)
  setTimeout(() => {
    // 3. Случайно выбрать карту (от 0 до 7)
    const randomIndex = Math.floor(Math.random() * cards.length);
    const selectedCard = cards[randomIndex];

    // 4. Заполнить экран результата
    renderCard(selectedCard);

    // 5. Показать экран результата
    showScreen("screen-result");
  }, 1800);
}
