// Показать нужный экран, скрыть остальные
function showScreen(id) {
  document.querySelectorAll(".screen").forEach((s) => s.classList.remove("active"));
  const el = document.getElementById(id);
  if (el) {
    el.classList.add("active");
    window.scrollTo({ top: 0, behavior: "smooth" });
  }
}

// Заполнить экран результата данными карты
function renderCard(card) {
  document.getElementById("result-theme").textContent = card.theme;
  document.getElementById("result-title").textContent = card.title;
  document.getElementById("result-subtitle").textContent = card.subtitle;
  document.getElementById("result-energy").textContent = card.energyDrain;
  document.getElementById("result-interpretation").textContent = card.interpretation;
  document.getElementById("result-action").textContent = card.action;
}

